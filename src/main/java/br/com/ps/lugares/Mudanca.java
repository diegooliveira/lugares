package br.com.ps.lugares;

public class Mudanca {

	private Lugar from;

	private Lugar to;

	private String name;

	public Lugar getFrom() {
		return from;
	}

	public void setFrom(Lugar from) {
		this.from = from;
	}

	public Lugar getTo() {
		return to;
	}

	public void setTo(Lugar to) {
		this.to = to;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
